    "def fcfs_schedule(df_in):\n",
    "    df = df_in.copy().sort_values(by='arrivalTime').reset_index(drop=True)\n",
    "    current_time = 0\n",
    "    start_times, finish_times = [], []\n",
    "    for arr, burst in zip(df['arrivalTime'], df['processTime']):\n",
    "        start = max(current_time, arr)\n",
    "        start_times.append(start)\n",
    "        finish = start + burst\n",
    "        finish_times.append(finish)\n",
    "        current_time = finish\n",
    "    df['StartTime'] = start_times\n",
    "    df['FinishTime'] = finish_times\n",
    "    df['Preemptions'] = 0\n",
    "    return df, current_time\n",
    "\n",
    "def priority_schedule(df_in): \n",
    "    # Non-preemptive, higher number = higher priority\n",
    "    df = df_in.copy().sort_values(by=['arrivalTime', 'priority'], ascending=[True, False]).reset_index(drop=True)\n",
    "    ready_queue = []\n",
    "    current_time = 0\n",
    "    completed = 0\n",
    "    n = len(df)\n",
    "    \n",
    "    start_times = np.zeros(n)\n",
    "    finish_times = np.zeros(n)\n",
    "    is_completed = np.zeros(n, dtype=bool)\n",
    "    \n",
    "    while completed < n:\n",
    "        arrived = df[(df['arrivalTime'] <= current_time) & (~is_completed)]\n",
    "        if arrived.empty:\n",
    "            next_arrival = df.loc[~is_completed, 'arrivalTime'].min()\n",
    "            current_time = next_arrival\n",
    "            arrived = df[(df['arrivalTime'] <= current_time) & (~is_completed)]\n",
    "            \n",
    "        # Pick highest priority\n",
    "        idx = arrived['priority'].idxmax()\n",
    "        start = current_time\n",
    "        burst = df.loc[idx, 'processTime']\n",
    "        finish = start + burst\n",
    "        \n",
    "        start_times[idx] = start\n",
    "        finish_times[idx] = finish\n",
    "        is_completed[idx] = True\n",
    "        \n",
    "        current_time = finish\n",
    "        completed += 1\n",
    "        \n",
    "    df['StartTime'] = start_times\n",
    "    df['FinishTime'] = finish_times\n",
    "    df['Preemptions'] = 0\n",
    "    return df, current_time\n",
    "\n",
    "def round_robin_schedule(df_in, quantum):\n",
    "    df = df_in.copy().sort_values(by='arrivalTime').reset_index(drop=True)\n",
    "    n = len(df)\n",
    "    remaining_burst = df['processTime'].values.copy()\n",
    "    start_times = np.full(n, -1.0)\n",
    "    finish_times = np.zeros(n)\n",
    "    preemptions = np.zeros(n)\n",
    "    \n",
    "    current_time = 0\n",
    "    completed = 0\n",
    "    queue = []\n",
    "    \n",
    "    idx_arrived = 0\n",
    "    \n",
    "    while completed < n:\n",
    "        while idx_arrived < n and df.loc[idx_arrived, 'arrivalTime'] <= current_time:\n",
    "            queue.append(idx_arrived)\n",
    "            idx_arrived += 1\n",
    "            \n",
    "        if not queue:\n",
    "            current_time = df.loc[idx_arrived, 'arrivalTime']\n",
    "            continue\n",
    "            \n",
    "        curr_proc = queue.pop(0)\n",
    "        if start_times[curr_proc] == -1:\n",
    "            start_times[curr_proc] = current_time\n",
    "            \n",
    "        time_to_run = min(quantum, remaining_burst[curr_proc])\n",
    "        current_time += time_to_run\n",
    "        remaining_burst[curr_proc] -= time_to_run\n",
    "        \n",
    "        while idx_arrived < n and df.loc[idx_arrived, 'arrivalTime'] <= current_time:\n",
    "            queue.append(idx_arrived)\n",
    "            idx_arrived += 1\n",
    "            \n",
    "        if remaining_burst[curr_proc] == 0:\n",
    "            finish_times[curr_proc] = current_time\n",
    "            completed += 1\n",
    "        else:\n",
    "            queue.append(curr_proc)\n",
    "            preemptions[curr_proc] += 1\n",
    "            \n",
    "    df['StartTime'] = start_times\n",
    "    df['FinishTime'] = finish_times\n",
    "    df['Preemptions'] = preemptions\n",
    "    return df, current_time"
