    "def calculate_extended_metrics(df, total_time, quantum=None):\n",
    "    df[\"TurnaroundTime\"] = df[\"FinishTime\"] - df[\"arrivalTime\"]\n",
    "    df[\"WaitingTime\"] = df[\"TurnaroundTime\"] - df[\"processTime\"]\n",
    "    df[\"ResponseTime\"] = df[\"StartTime\"] - df[\"arrivalTime\"]\n",
    "    \n",
    "    metrics = {}\n",
    "    metrics['AWT'] = df[\"WaitingTime\"].mean()\n",
    "    metrics['ATAT'] = df[\"TurnaroundTime\"].mean()\n",
    "    metrics['Response Time'] = df[\"ResponseTime\"].mean()\n",
    "    metrics['MWT'] = df[\"WaitingTime\"].max()\n",
    "    metrics['Throughput'] = len(df) / total_time if total_time > 0 else 0\n",
    "    \n",
    "    total_burst = df[\"processTime\"].sum()\n",
    "    metrics['CPU Utilization (%)'] = (total_burst / total_time) * 100 if total_time > 0 else 0\n",
    "    \n",
    "    sum_wt = df[\"WaitingTime\"].sum()\n",
    "    sum_sq_wt = (df[\"WaitingTime\"]**2).sum()\n",
    "    n = len(df)\n",
    "    metrics['JFI'] = (sum_wt**2) / (n * sum_sq_wt) if sum_sq_wt > 0 else 1.0\n",
    "    \n",
    "    def gini(array):\n",
    "        array = np.sort(array)\n",
    "        index = np.arange(1, array.shape[0] + 1)\n",
    "        nn = array.shape[0]\n",
    "        return (np.sum((2 * index - nn  - 1) * array)) / (nn * np.sum(array)) if np.sum(array) > 0 else 0\n",
    "    metrics['Gini (WT)'] = gini(df[\"WaitingTime\"].values)\n",
    "    \n",
    "    avg_burst = df[\"processTime\"].mean()\n",
    "    starvation_threshold = 3 * avg_burst\n",
    "    metrics['Starvation Count'] = len(df[df[\"WaitingTime\"] > starvation_threshold])\n",
    "    \n",
    "    metrics['Context-Switches'] = len(df)\n",
    "    if 'Preemptions' in df.columns:\n",
    "        metrics['Context-Switches'] += df['Preemptions'].sum()\n",
    "        \n",
    "    return metrics"
